/*program to display the message if the answer of the question is y
using if selection*/
#include <stdio.h>
void main()
{
    char a;
    printf("\nDo you want to display (y/n): ");
    scanf("%c",&a);
    if(a=='y')
    {
        printf("\nDepartment of Computer Science and Engineering");
        printf("\nDaffodil International University");
        printf("\nDhanmondi");
        printf("\nDhaka\n\n\n\n\n\n");
    }
}
